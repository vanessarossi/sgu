package br.coop.unimedriopardo.sgu.util.view.fluxo;

public class QuartoNivelDemonstrativoValorizado {

	private String descricao;
	private String valorAnterior;
	private String valor;
	private String valorPrevisto;
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getValorAnterior() {
		return valorAnterior;
	}
	public void setValorAnterior(String valorAnterior) {
		this.valorAnterior = valorAnterior;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public String getValorPrevisto() {
		return valorPrevisto;
	}
	public void setValorPrevisto(String valorPrevisto) {
		this.valorPrevisto = valorPrevisto;
	}
}
